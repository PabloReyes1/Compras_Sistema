 public static void buscarEnTabla(JTable tabla, JTextField txt_busqueda) {
        DefaultTableModel tableModel = (DefaultTableModel) tabla.getModel();
        TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(tableModel);
        tabla.setRowSorter(sorter);

        String textoBusqueda = txt_busqueda.getText();
        if (textoBusqueda.trim().length() == 0) {
            sorter.setRowFilter(null); // Mostrar todos los datos si el campo de búsqueda está vacío
        } else {
            sorter.setRowFilter(RowFilter.regexFilter("(?i)" + textoBusqueda)); // Filtrar sin distinción entre mayúsculas y minúsculas
        }
    }
