    private void btn_verFacturaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_verFacturaActionPerformed
  
  int selectedRow = tabla_facturas.getSelectedRow();

    if (selectedRow != -1) {
        Object valor = tabla_facturas.getValueAt(selectedRow, 0);
        String valorStr = valor.toString();
        
        // Crea una instancia del otro JFrame (OtroFrame)
        VERFACTURAS otroFrame = new VERFACTURAS();
        
        // Llama a un método en OtroFrame para establecer el valor en el JTextField
        otroFrame.setTextoEnJTextField(valorStr);
        otroFrame.setVisible(true);
        dispose();
    } else {
        // Si no hay una fila seleccionada, muestra un mensaje de error o realiza alguna otra acción
        JOptionPane.showMessageDialog(this, "Por favor, selecciona una fila en la tabla");
    }
    }//GEN-LAST:event_btn_verFacturaActionPerformed

