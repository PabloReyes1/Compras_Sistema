    private void btn_agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_agregarActionPerformed
        int select = tabla_productos.getSelectedRow();
        try {
            String id, nombre, descripsion, precio, cantidad, importe;
            double calcula = 0.0, x = 0.0, iva = 0.0;
            int can = 0;

            if (select == -1) {
                JOptionPane.showMessageDialog(null, "Debe seleccionar un producto", "Advertencia", JOptionPane.WARNING_MESSAGE);
            } else {
                id = tabla_productos.getValueAt(select, 0).toString();
                nombre = tabla_productos.getValueAt(select, 1).toString();
                descripsion = tabla_productos.getValueAt(select, 2).toString();
                precio = tabla_productos.getValueAt(select, 3).toString();
                cantidad = txt_cantidad.getText();

                if (cantidad.isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Debe colocar la cantidad de productos", "Advertencia", JOptionPane.WARNING_MESSAGE);
                } else {
                    // Cálculos
                    x = (Double.parseDouble(precio) * Integer.parseInt(cantidad));
                    importe = String.valueOf(x);

                    m = (DefaultTableModel) tabla_descripcion.getModel();
                    String filaelemento[] = {id, cantidad, descripsion, precio, importe};
                    m.addRow(filaelemento);

                    calcula = (Double.parseDouble(precio));
                    total = total + calcula;
                    iva = total * 0.12;
                    iva = iva;
                    sub_total = total - iva;

                    txt_total.setText("" + total);
                    txt_sub.setText("" + sub_total);
                    txt_iva.setText("" + iva);

                    // Mostrar mensaje de éxito y vaciar el campo de cantidad
                    JOptionPane.showMessageDialog(null, "Producto agregado con éxito", "Éxito", JOptionPane.INFORMATION_MESSAGE);
                    txt_cantidad.setText(""); // Vaciar el campo de cantidad
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btn_agregarActionPerformed

