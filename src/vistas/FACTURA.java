    private String incrementarNumeroFactura(String noFactura) {
        int numero = Integer.parseInt(noFactura.substring(2));
        numero++;
        return String.format("no%04d", numero);
    }

    private String obtenerNumeroFactura() {
        String noFactura = null;

        try (Connection conn = ConexionSQL.conectar()) {
            // Consulta SQL para obtener el último número de factura
            String sql = "SELECT MAX(no_factura) FROM Factura";
            Statement stmt = conn.createStatement();
            ResultSet resultSet = stmt.executeQuery(sql);

            if (resultSet.next()) {
                noFactura = resultSet.getString(1);
                if (noFactura != null) {
                    // Incrementa el número de factura para la siguiente factura
                    noFactura = incrementarNumeroFactura(noFactura);
                } else {
                    // Si no hay registros en la base de datos, establece el primer número
                    noFactura = "no0001";
                }
            }
        } catch (SQLException e) {
            System.out.println("Error al obtener el número de factura desde la base de datos: " + e);
        }

        return noFactura;
    }

