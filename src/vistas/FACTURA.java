    private void btn_crearfacturaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_crearfacturaActionPerformed

  try {
        int idProveedor = 0;  
        int idSucursal = 0;  
        Object selectedProveedor = txt_proveedor.getSelectedItem();
        Object selectedSucursal = txt_idsucursal.getSelectedItem();
        String estado = txt_estado.getSelectedItem().toString();
        String numeroFactura = txt_nofac.getText();
        double precioTotal = Double.parseDouble(txt_total.getText());
        DefaultTableModel model1 = (DefaultTableModel) tabla_descripcion.getModel();
        int rowCount = model1.getRowCount();

        // Validación de JComboBox
        if (selectedProveedor == null || selectedProveedor.toString().equals("Seleccione proveedor:") ||
            selectedSucursal == null || selectedSucursal.toString().equals("Seleccione sucursal:") ||
            estado.equals("SELECCIONE ESTADO")) {
            JOptionPane.showMessageDialog(this, "Faltan parámetros por escoger en los ComboBox.");
            return; // Detener la ejecución si falta alguna selección en los JComboBox.
        } else {
            idProveedor = buscarIdProveedor(selectedProveedor.toString());
            idSucursal = buscarIdSucursal(selectedSucursal.toString());
            
            for (int i = 0; i < rowCount; i++) {
                String nombreProducto = model1.getValueAt(i, 2).toString();
                int idProducto = Integer.parseInt(model1.getValueAt(i, 0).toString());
                int cantidad = Integer.parseInt(model1.getValueAt(i, 1).toString());
                double precioUnitarioIVA = Double.parseDouble(model1.getValueAt(i, 3).toString());
                Procesos_Almacenados.SPFactura.insertarFacturas(idProveedor, idSucursal, estado, numeroFactura, idProducto, nombreProducto, precioTotal, cantidad, precioUnitarioIVA);
            }
            JOptionPane.showMessageDialog(this, "Factura generada");
            Mostrarcombobox();
            mostrarNumeroFactura();
            model1.setRowCount(0);
        }
    } catch (NumberFormatException e) {
        // Manejar errores de conversión de datos, por ejemplo, valores no válidos en los campos de entrada.
        JOptionPane.showMessageDialog(this, "Error en los datos de entrada");
    }
    }//GEN-LAST:event_btn_crearfacturaActionPerformed


